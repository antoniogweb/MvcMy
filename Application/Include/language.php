class Lang
{
	public static $allowed = array('it');
	public static $current = 'it';
	public static $edit = false;
	
	public static function sanitize($lang = 'it')
	{
		return (in_array($lang,self::$allowed)) ? sanitizeAll($lang) : 'en';
	}

	public static $i18n = array();
}
